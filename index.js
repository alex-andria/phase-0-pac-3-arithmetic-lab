function add(num1, num2){
    let total = num1 + num2;
    return total;
}

function subtract(num1, num2){
    let total = num1 - num2;
    return total;
}

function multiply(num1, num2){
    let total = num1 * num2;
    return total;
}

function divide(num1, num2){
    let total = num1 / num2;
    return total;
}

function increment(n){
    let total = n+=1;
    return total;
}

function decrement(n){
    let total = n-=1;
    return total;
}

function makeInt(n){
    let total = parseInt(n,10);
    return total;
}

function preserveDecimal(n){
    let total = parseFloat(n);
    return total;
}